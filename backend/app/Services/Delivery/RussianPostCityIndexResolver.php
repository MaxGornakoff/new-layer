    public function resolve(?string $cityName): ?string
    {
        if (! filled($cityName)) {
            return null;
        }

        $normalized = $this->normalize($cityName);

        if ($normalized === '') {
            return null;
        }

        if (isset(self::CITY_INDEXES[$normalized])) {
            return self::CITY_INDEXES[$normalized];
        }

        foreach (self::CITY_INDEXES as $city => $index) {
            if (str_contains($normalized, $city) || str_contains($city, $normalized)) {
                return $index;
            }
        }

        return null;
    }

    private function normalize(string $cityName): string
    {
        $value = mb_strtolower(trim($cityName));
        $value = preg_replace('/^(г\.|город)\s+/u', '', $value) ?? $value;
        $value = str_replace(['ё'], ['е'], $value);
        $value = preg_replace('/\s+/u', ' ', $value) ?? $value;

        return trim($value);
    }
